// Create reverse mapping for quick lookup
const variantToBase = {};
Object.entries(animalVariants).forEach(([base, data]) => {
  // Map base name to itself
  variantToBase[base] = base;
  
  // Map all variants to base name
  data.variants.forEach(variant => {
    variantToBase[variant] = base;
  });
});

/**
 * Normalizes an animal name to its base form
 * @param {string} animalName - The input animal name (could be base or variant)
 * @returns {string|null} - The base animal name, or null if not found
 */
function normalizeAnimalName(animalName) {
  if (!animalName || typeof animalName !== 'string') {
    return null;
  }
  
  // Trim and normalize the input
  const normalized = animalName.trim();
  
  // Look up in the mapping
  return variantToBase[normalized] || null;
}

/**
 * Checks if a given name is a known animal (base or variant)
 * @param {string} animalName - The input animal name
 * @returns {boolean} - True if the name is a known animal or variant
 */
function isKnownAnimal(animalName) {
  return normalizeAnimalName(animalName) !== null;
}

/**
 * Gets all variants for a base animal name
 * @param {string} baseAnimalName - The base animal name
 * @returns {string[]|null} - Array of variants, or null if not found
 */
function getAnimalVariants(baseAnimalName) {
  const animalData = animalVariants[baseAnimalName];
  return animalData ? animalData.variants : null;
}

/**
 * Gets all base animal names
 * @returns {string[]} - Array of all base animal names
 */
function getAllBaseAnimals() {
  return Object.keys(animalVariants);
}

